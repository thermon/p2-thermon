function getElement(id) {
//	// alert(id);
	if (typeof(id) == "string") {
		if (isIE) { // IE用
			return document.all[id];
		} else if (document.getElementById) { // DOM対応用（Mozilla）
			return document.getElementById(id);
		}
	} else {
		return id;
	}
}

